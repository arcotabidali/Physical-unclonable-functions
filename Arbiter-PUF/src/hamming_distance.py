def hamming_distance(str1, str2):
    # Ensure both strings have the same length
    if len(str1) != len(str2):
        raise ValueError("Input strings must have the same length")
    
    # Calculate Hamming Distance
    return sum(el1 != el2 for el1, el2 in zip(str1, str2))

def single_chip_hamming_distance(responses):
    ans = [0 for _ in range(16)]
    for i in range(16):
        hd = 0
        for j in range(16):
            for k in range(j+1,16):
                hd += hamming_distance(responses[i][j],responses[i][k])
        ans[i] = hd/(8*15)
        ans[i] = ans[i]/32 * 100
    return ans

def multi_chip_hamming_distance(responses):
    hd = 0
    for i in range(16):
        for j in range(i+1,16):
            for k in range(16):
                hd += hamming_distance(responses[i][k],responses[j][k])
    ans = hd/(16*8*15)
    ans = ans/32 * 100
    return ans


